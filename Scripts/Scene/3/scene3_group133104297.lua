			return -1
		end
	
	return 0
end

-- 触发操作
function action_EVENT_TIME_AXIS_PASS_297044(context, evt)
	-- 触发镜头注目，注目位置为坐标（378，215，-71），持续时间为3秒，并且为强制注目形式，不广播其他玩家
