-- 触发条件
function condition_EVENT_ENTER_REGION_191011(context, evt)
	if evt.param1 ~= 191011 then return false end
	
	-- 判断角色数量不少于1
	if ScriptLib.GetRegionEntityCount(context, { region_eid = evt.source_eid, entity_type = EntityType.AVATAR }) < 1 then
		return false
	end
	
	return true
end

